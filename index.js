const gracefulShutdown = async (signal) => {
    console.log(`\n‚ö†Ô∏è  ${signal} recibido. Cerrando servidor...`);

    try {
        // Detener CRON jobs si existen
        const { stopAllCronJobs } = await import('./cron/index.js');
        stopAllCronJobs();
        console.log('‚úÖ CRON jobs detenidos');
    } catch (error) {
        // CRON jobs no disponibles
    }

    // Cerrar servidor HTTP
    if (server) {
        await new Promise((resolve) => {
            server.close(() => {
                console.log('‚úÖ Servidor cerrado correctamente');
                resolve();
            });
        });
    }

    // Cerrar conexi√≥n a MongoDB
    if (mongoose.connection.readyState === 1) {
        await mongoose.connection.close(false);
        console.log('‚úÖ MongoDB desconectado');
    }

    process.exit(0);

    // Forzar cierre despu√©s de 10 segundos
    setTimeout(() => {
        console.error('‚ö†Ô∏è  Cierre forzado despu√©s de 10 segundos');
        process.exit(1);
    }, 10000);
};

process.on('SIGTERM', () => gracefulShutdown('SIGTERM'));
process.on('SIGINT', () => gracefulShutdown('SIGINT (Ctrl+C)'));

// Capturar errores no manejados
process.on('uncaughtException', (error) => {
    console.error('‚ùå Uncaught Exception:', error);
    gracefulShutdown('UNCAUGHT_EXCEPTION');
});

process.on('unhandledRejection', (reason, promise) => {
    console.error('‚ùå Unhandled Rejection:', reason);
    gracefulShutdown('UNHANDLED_REJECTION');
});

